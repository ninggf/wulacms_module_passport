    }

    public function updateData($data = null, $con = null, $cb = null) {
        return $this->update($data, $con, $cb); // TODO: Change the autogenerated stub
    }

    public function insertsData($datas, \Closure $cb = null) {
        return $this->inserts($datas, $cb); // TODO: Change the autogenerated stub
    }

}