namespace passport\classes\model;

use wulaphp\db\MetaTable;
use wulaphp\db\Table;

class PassportMeta extends Table {
    use MetaTable;
    private $metaIdField   = 'passport_id';
    private $metaNameField = 'name';

    public function get_field($passport_id, $name) {
        $res = $this->findOne(['passport_id' => $passport_id, 'name' => $name], 'value')[0];
        if ($res) {
            return $res['value'];
        }

        return false;
    }

    public function insertData($data, $cb = null) {
